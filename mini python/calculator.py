
class calculator:
    
    def add(a,b):
        return a+b
    
    def subtract(x,y):
        return x-y
    
    def multiply(i,j):
        return i*j
    
    def divide(f,g):
        return f/g
    
my_function=calculator
add=my_function.add(5,3)
print(add)
sub=my_function.subtract(10,3)
print(sub)
multi=my_function.multiply(5,3)
print(multi)
div=my_function.divide(125,5)
print(div)
